public class loops14{
   public static void main(String[]args){
       //for loop
       //outter loop
    for(int i =  1; i <=3; i++){
        //inner loop
       for(int x = 1; x <= i ; x++){
        System.out.print(x +" ");
        }
        System.out.println();           
    }
    }
}
